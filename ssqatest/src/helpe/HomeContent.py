    what_defi = 'What is DeFi?'
    what_defi_answer = 'DeFi stands for “decentralized finance” which is an overall term to describe a variety of financial applications in cryptocurrency or blockchain – enabling application of decentralization on financial intermediaries such as banks. You’re all aware of how centralized financial institutions operate. They hold all the money deposited by you and other customers and whenever you need to send the money to someone or a loan from the bank you’re subjected to a long, bureaucratic, and tedious process. Other than the inefficiency, the bank takes some percentage of the money in terms of transaction fee. What DeFi does is to make financial transactions faster, cheaper, and generally more efficient: A DeFi application makes it easy for people to transact money without the need to trust one another. That is, DeFi platforms are built on blockchain to help facilitate financial transactions with the help of smart contracts. DeFi is allowing crypto entrepreneurs to recreate traditional financial instruments in a decentralized architecture, outside of banks or central authorities’ control.'

    what_smart_contract = 'What are Smart Contracts?'
    what_smart_contract_answer = 'Smart Contracts are automated applications that act as intermediaries. This is a piece of code in the blockchain that helps peers (humans and applications such as other smart contracts) to interact on the platform without the need to trust each other. The code of Smart Contracts is always available to the public, that’s why the blockchain technology is often called “trustless”'

    what_liquidity_meaning = 'What Is Liquidity Mining?'
    what_liquidity_meaning_answer = 'Liquidity mining means adding tokens to liquidity pools of DEXs (decentralized exchanges) such as Uniswap, Sushiswap or PancakeSwap. By providing liquidity to the market, you allow the decentralized exchange to function, and in return you are rewarded with cryptocurrency. Adding liquidity to decentralized exchanges is called “liquidity mining” because it can bring you profits just like mining cryptocurrencies such as Bitcoin or Ethereum. Both liquidity mining and mining crypto are excellent ways of generating blockchain-based passive income.'

    what_liquidity_pool = 'What exactly are “liquidity pools”?'
    what_liquidity_pool_answer = 'When you use a centralized exchange like Binance or Coinbase, the liquidity is provided by the company which owns the exchange. Since decentralized exchanges don’t have their own reserves, liquidity is provided by the users who lock their own funds in liquidity pools. Simply put, liquidity pools are what allows you to buy and sell crypto on DEXs.'

    what_dao = 'What is a Decentralized Autonomous Organization (DAO)?'
    what_dao_answer = 'A decentralized autonomous organization (DAO) is a software running on a blockchain that offers users a built-in model for the collective management of its code. DAOs differ from traditional organizations managed by boards, committees and executives. Rather than being governed by a limited group, DAOs use a set of rules written down in code and enforced by the network of computers running a shared software. To become a member of a DAO, users need to first join the DAO by buying its cryptocurrency. Holding the asset then generally gives users the power to vote on proposals and updates, proportional to the amount they hold.'

    what_dao_governance = 'What is DAO Governance?'
    what_dao_governance_answer = 'DAO governance is coordinated using tokens or NFTs that grant voting powers. Admission to a DAO is limited to people who have a confirmed ownership of these governance tokens in a cryptocurrency wallet, and membership can be exchanged. Governance is conducted through a series of proposals that members vote on through the blockchain, and the possession of more governance tokens often translates to greater voting power.'

